    private void btnEliminarCombActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCombActionPerformed

        if (this.lbl_IDComb.getText() != "") {
            int dificultad = Integer.valueOf(this.lbl_IDComb.getText());

            int des = JOptionPane.showConfirmDialog(this, "Esta seguro de que desea eliminar este registro");
            if (des == 0) {
                if (cd.eliminarCombinacion(dificultad)) {
                    JOptionPane.showMessageDialog(this, "Se Elimino");
                    limipar2(dificultad);
                } else {
                    JOptionPane.showMessageDialog(this, "No se elmino");
                }
            }

        }
    }//GEN-LAST:event_btnEliminarCombActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (this.txtTiempo.getText() != "" && this.txtPuntos_Tiempo.getText() != "" && this.txtPuntos_Acierto.getText() != "") {
            int tiempo = Integer.parseInt(this.txtTiempo.getText());
            int puntos_tiempo = Integer.parseInt(this.txtPuntos_Tiempo.getText());
            int puntos_acierto = Integer.parseInt(this.txtPuntos_Acierto.getText());
            int id = Integer.parseInt(this.lbl_ID.getText());

            cf = new Configuracion(id, tiempo, puntos_tiempo, puntos_acierto);
            if (cd.guardarConfiguracion(cf)) {
                JOptionPane.showMessageDialog(this, "Se guardo");
                mostrarConfiguraciones(id);
            } else {
                JOptionPane.showMessageDialog(this, "No se Guardo");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese todos los datos");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnAgregarCombActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCombActionPerformed
        if (this.lbl_ID.getText() != "") {
            if (this.txtCombinacion.getText() != "") {
                ButtonModel seleccion = buttonGroup1.getSelection();
                if (seleccion != null) {
                    int dificultad = Integer.valueOf(this.lbl_ID.getText());
                    String combinacion = this.txtCombinacion.getText();
                    if (this.player1.isSelected()) {
                        c = new Combinacion(combinacion, 1, dificultad);

                    } else if (this.player2.isSelected()) {
                        c = new Combinacion(combinacion, 2, dificultad);
                    }
                    if (cd.agregarCombinacion(c)) {
                        JOptionPane.showMessageDialog(this, "Se Agrego");
                        limipar2(dificultad);

                    } else {
                        JOptionPane.showMessageDialog(this, "No Se Agrego");
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Seleccione un Jugador");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Llene todos los campos");
            }
        }

    }//GEN-LAST:event_btnAgregarCombActionPerformed

