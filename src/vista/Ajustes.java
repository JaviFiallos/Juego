    private void cargarTabla(int dificultad) {

        modelo = (DefaultTableModel) tablaCombinaciones.getModel();
        List<Combinacion> lp = cd.listarCombinaciones(dificultad);
        for (Combinacion p : lp) {
            modelo.addRow(new Object[]{p.getId(), p.getCombinacion(), p.getPlayer()});
        }
        this.tablaCombinaciones.setModel(modelo);

    }

    public void limpiarTabla(DefaultTableModel model) {
        for (int i = 0; i < model.getRowCount(); i++) {
            model.setRowCount(0);
        }
    }

    private void mostrarConfiguraciones(int id) {
        Configuracion c = cd.listarConfiguraciones(id);
        this.txtTiempo.setText(String.valueOf(c.getTiempo()));
        this.txtPuntos_Tiempo.setText(String.valueOf(c.getPunto_Tiempo()));
        this.txtPuntos_Acierto.setText(String.valueOf(c.getPunto_Acierto()));
    }

    private void limpiar(int id) {
        this.lbl_ID.setText(String.valueOf(id));
        this.lbl_IDComb.setText("");
        this.txtCombinacion.setText("");
        mostrarConfiguraciones(id);
        limpiarTabla(modelo);
        cargarTabla(id);
    }

    private void limipar2(int id) {

        this.lbl_IDComb.setText("");
        this.txtCombinacion.setText("");
        limpiarTabla(modelo);
        cargarTabla(id);
    }



